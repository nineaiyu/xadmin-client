              type="primary"
              :size="size"
              :icon="useRenderIcon(Picture)"
              @click="handleUpload(row)"
            >
              {{ t("MoviesActor.updateAvatar") }}
            </el-button>
          </template>
        </pure-table>
      </template>
    </PureTableBar>
  </div>
</template>
